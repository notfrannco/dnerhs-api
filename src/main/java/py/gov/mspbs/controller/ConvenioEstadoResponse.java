package py.gov.mspbs.controller;

public class ConvenioEstadoResponse {

    String intitucionFormadora;
    String rector;
    String director;
    String decano;
    String docentes;
    String ingresosEgresos;
    String carrerasProgramas;
    String materias;
    String tutores;
    String estudiantes;
    String practicas;
    String documentacion;

    public String getIntitucionFormadora() {
        return intitucionFormadora;
    }

    public void setIntitucionFormadora(String intitucionFormadora) {
        this.intitucionFormadora = intitucionFormadora;
    }

    public String getRector() {
        return rector;
    }

    public void setRector(String rector) {
        this.rector = rector;
    }

    public String getDirector() {
        return director;
    }

    public void setDirector(String director) {
        this.director = director;
    }

    public String getDecano() {
        return decano;
    }

    public void setDecano(String decano) {
        this.decano = decano;
    }

    public String getDocentes() {
        return docentes;
    }

    public void setDocentes(String docentes) {
        this.docentes = docentes;
    }

    public String getIngresosEgresos() {
        return ingresosEgresos;
    }

    public void setIngresosEgresos(String ingresosEgresos) {
        this.ingresosEgresos = ingresosEgresos;
    }

    public String getCarrerasProgramas() {
        return carrerasProgramas;
    }

    public void setCarrerasProgramas(String carrerasProgramas) {
        this.carrerasProgramas = carrerasProgramas;
    }

    public String getMaterias() {
        return materias;
    }

    public void setMaterias(String materias) {
        this.materias = materias;
    }

    public String getTutores() {
        return tutores;
    }

    public void setTutores(String tutores) {
        this.tutores = tutores;
    }

    public String getEstudiantes() {
        return estudiantes;
    }

    public void setEstudiantes(String estudiantes) {
        this.estudiantes = estudiantes;
    }

    public String getPracticas() {
        return practicas;
    }

    public void setPracticas(String practicas) {
        this.practicas = practicas;
    }

    public String getDocumentacion() {
        return documentacion;
    }

    public void setDocumentacion(String documentación) {
        this.documentacion = documentación;
    }

}


